using namespace std;

void lab_4_4() {
	double a, b;
	cout << "a = ";
	cin >> a;
	cout << "b = ";
	cin >> b;
	cout << endl << "Сумма квадратов = " << a * a + b * b << endl;
	cout << "Разность квадратов = " << a * a - b * b << endl;
	cout << "Произведение квадратов = " << (a * a) * (b * b) << endl;
	cout << "Частное квадратов = " << (a * a) / (b * b) << endl;
}